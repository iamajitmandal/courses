# Learning about Git Branch

'''
    To check git branch
        git branch

    To make new branch
        git branch branch_name

        e.g git branch test -> creates branch named 'test'

    To move to another branch
        git checkout branch_name

        e.g. git checkout test -> moves to 'test' branch

        Now if we add any code then that adds to 'test' branch

    Branches are deleted only from remote (means from github)

    To DOWNLOAD codes from remote:
        git pull origin main

    Advanced Topics:
        COMMIT SQUASH
'''

